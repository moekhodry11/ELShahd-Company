// Language switching functionality
class LanguageManager {
  constructor() {
    this.currentLang = localStorage.getItem("preferredLanguage") || "en";
    this.init();
  }

  init() {
    this.setLanguage(this.currentLang);
    this.bindEvents();
  }

  bindEvents() {
    // Language switcher buttons
    document.addEventListener("click", (e) => {
      if (e.target.classList.contains("lang-btn")) {
        const lang = e.target.getAttribute("data-lang");
        this.setLanguage(lang);
      }
    });
  }

  setLanguage(lang) {
    this.currentLang = lang;
    localStorage.setItem("preferredLanguage", lang);

    // Update HTML attributes
    const htmlRoot =
      document.getElementById("html-root") || document.documentElement;
    htmlRoot.setAttribute("lang", lang);
    htmlRoot.setAttribute("dir", lang === "ar" ? "rtl" : "ltr");

    // Update active language button
    this.updateLanguageButtons();

    // Translate all elements
    this.translatePage();

    // Apply RTL/LTR specific adjustments
    this.applyDirectionStyles();
  }

  updateLanguageButtons() {
    const langButtons = document.querySelectorAll(".lang-btn");
    langButtons.forEach((btn) => {
      btn.classList.toggle(
        "active",
        btn.getAttribute("data-lang") === this.currentLang
      );
    });
  }

  translatePage() {
    const elementsToTranslate = document.querySelectorAll("[data-translate]");

    elementsToTranslate.forEach((element) => {
      const key = element.getAttribute("data-translate");
      const translation = this.getTranslation(key);

      if (translation) {
        // Handle different element types
        if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
          if (element.type === "submit" || element.type === "button") {
            element.value = translation;
          } else {
            element.placeholder = translation;
          }
        } else {
          element.textContent = translation;
        }
      }
    });

    // Handle elements with data-translate-html for HTML content
    const htmlElements = document.querySelectorAll("[data-translate-html]");
    htmlElements.forEach((element) => {
      const key = element.getAttribute("data-translate-html");
      const translation = this.getTranslation(key);
      if (translation) {
        element.innerHTML = translation;
      }
    });
  }

  getTranslation(key) {
    const keys = key.split(".");
    let translation = translations[this.currentLang];

    for (const k of keys) {
      if (translation && translation[k]) {
        translation = translation[k];
      } else {
        console.warn(`Translation not found for key: ${key}`);
        return null;
      }
    }

    return translation;
  }

  applyDirectionStyles() {
    const body = document.body;

    if (this.currentLang === "ar") {
      body.classList.add("rtl");
      body.classList.remove("ltr");
    } else {
      body.classList.add("ltr");
      body.classList.remove("rtl");
    }
  }

  // Method to get current language
  getCurrentLanguage() {
    return this.currentLang;
  }

  // Method to get translation for JavaScript use
  t(key) {
    return this.getTranslation(key);
  }
}

// Initialize language manager when DOM is loaded
document.addEventListener("DOMContentLoaded", () => {
  window.languageManager = new LanguageManager();
});

// Export for use in other files
if (typeof module !== "undefined" && module.exports) {
  module.exports = { translations, LanguageManager };
}
